<h2>Welcome</h2>

<p>
Welcome to the home page of <abbr title="Ingress Intel Total Conversion">IITC</abbr>.
</p>

<p>
IITC is a browser add-on that modifies the Ingress intel map. It is faster than the standard site, and
offers many more features. It is available for
<a href="?page=desktop">desktop browsers</a>, such as Chrome and Firefox, and as a
<a href="?page=mobile">mobile application</a>.
</p>

<h3>Latest news</h3>

<h4>29th April 2014</h4>
<p>
IITC 0.16.7 and IITC Mobile 0.10.7 have been released, to fix things to work with the latest Niantic changes.
</p>
<p>
There have also been a few tweaks to improve performance, and an optional alternative rendering mode
that might give a good performance boost in some cases when there's lots of portals on screen.
</p>

